// Helper functions
export function getStudentById(id: string): Student | undefined {
  return MOCK_STUDENTS.find((student) => student.id === id)
}

export function getPaymentsForStudent(studentId: string): Payment[] {
  return MOCK_PAYMENTS.filter((payment) => payment.studentId === studentId)
}

export function getFeeStructureById(id: string): FeeStructure | undefined {
  return MOCK_FEE_STRUCTURES.find((fee) => fee.id === id)
}

export function getFeeStructureByLevelAndType(
  level: string,
  enrollmentType: string
): FeeStructure | undefined {
  return MOCK_FEE_STRUCTURES.find(
    (fee) => fee.level === level && fee.enrollmentType === enrollmentType
  )
}

export function getScholarshipById(id: string): Scholarship | undefined {
  return MOCK_SCHOLARSHIPS.find((scholarship) => scholarship.id === id)
}

export function getSessionById(id: string): AcademicSession | undefined {
  return MOCK_SESSIONS.find((session) => session.id === id)
}

export function getCurrentSession(): AcademicSession | undefined {
  return MOCK_SESSIONS.find((session) => session.isCurrent)
}

export function getStudentsForSession(sessionId: string): Student[] {
  return MOCK_STUDENTS.filter((student) => student.sessionId === sessionId)
}

export function getScholarshipRecipients(scholarshipId: string): Student[] {
  return MOCK_STUDENTS.filter((student) => student.scholarshipId === scholarshipId)
}

export function getAcademicRecordsForStudent(studentId: string): AcademicRecord[] {
  return MOCK_ACADEMIC_RECORDS.filter((record) => record.studentId === studentId)
}

export function getCourseEnrollmentsForStudent(studentId: string): CourseEnrollment[] {
  return MOCK_COURSE_ENROLLMENTS.filter((enrollment) => enrollment.studentId === studentId)
}

export function getCourseEnrollmentsForSemester(studentId: string, semesterId: string): CourseEnrollment[] {
  return MOCK_COURSE_ENROLLMENTS.filter(
    (enrollment) => enrollment.studentId === studentId && enrollment.semesterId === semesterId
  )
}

export function getCourseById(id: string): Course | undefined {
  return MOCK_COURSES.find((course) => course.id === id)
}

export function getSemesterById(id: string): Semester | undefined {
  return MOCK_SEMESTERS.find((semester) => semester.id === id)
}

export function getSemestersForSession(sessionId: string): Semester[] {
  return MOCK_SEMESTERS.filter((semester) => semester.academicSessionId === sessionId)
}
